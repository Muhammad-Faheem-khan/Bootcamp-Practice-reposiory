var arr = ['apple', 'mango', 'banana', 'pineapple', 'watermelon', 'melon', 'cherry']

// Q1: first element of list
console.log('First element: ', arr[0])

// Q2: length of list
console.log("Array Length: ", arr.length)

//Q3: last element of list which is at 6th index as we know index start from 0
console.log("Last Element: ", arr[arr.length-1])

//Q4: second last element of list
console.log("Second last Element: ", arr[arr.length-2])

//Q5: middle element of list
console.log("Middle Element: ", arr[Math.trunc(arr.length/2)])